namespace RobotWars
{
    /// <summary>
    /// Enum <c>Turn</c> describes which way the robot is turning. 
    /// Either R (right) or L (left)
    /// </summary>
    enum Turn
    {
        /// <summary>
        /// Right
        /// </summary>
        R = 1,
        /// <summary>
        /// Left
        /// </summary>
        L = -1
    }
}